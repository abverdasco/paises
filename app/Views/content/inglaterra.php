                        Su territorio está formado geográficamente por la parte sur y central de Gran Bretaña, isla que comparte junto a Escocia y Gales, y cerca de 100 
                        islas más pequeñas como las islas Sorlingas y la isla de Wight. Su capital y ciudad más poblada es Londres. Limita al norte con Escocia, al oeste 
