                        ha estado habitado por varias culturas desde hace cerca de 35 000 años. Toma su nombre de los anglos, uno de los pueblos germánicos que se 
                        establecieron en el lugar durante los siglos v y vi. Se convirtió en un Estado unificado en el año 927 y desde la era de los descubrimientos, 
                        que comenzó en el siglo xv, ha tenido un gran impacto cultural y legal en todo el mundo. El idioma inglés, la Iglesia anglicana y el Derecho de 
                        Inglaterra —tomado como base para el sistema jurídico de muchos otros países del mundo— se desarrollaron en Inglaterra, y el sistema parlamentario 
                        de gobierno ha sido mayormente adoptado por otras naciones. El territorio de Inglaterra está formado, principalmente por colinas bajas y llanuras, 
                        sobre todo en el centro y el sur del país. Sin embargo, hay terrenos de montaña en el norte (por ejemplo, el parque nacional del Distrito de los 
                        Lagos y los Peninos) y en el oeste (por ejemplo, Dartmoor y las colinas de Shropshire). La capital es Londres, que tiene la mayor área metropolitana 
